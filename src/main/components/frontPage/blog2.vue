            $this
              .find("li.last")
              .prev()
              .hasClass("active")
          ) {
            return;
          } else {
            var $a = $(".active", $this);

            if (!$a.next().hasClass("last") && !$ul.is(":animated")) {
              $a.removeClass("active")
                .next()
                .addClass("active");
            }

            if (!$ul.is(":animated")) {
              $ul.animate(
                {
                  left:
                    parseInt($ul.css("left"), 10) -
                    $ul.find("li").outerWidth(true)
                },
                duration
              );
            }
          }
