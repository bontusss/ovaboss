        var $this = $(this);

        var outer_width = $(window).outerWidth();
        var container_width;

        if (outer_width > 1145) {
          container_width = 1500;
        }

        if (outer_width > 960 && outer_width < 1145) {
          container_width = 960;
        }

        if (outer_width < 960) {
          container_width = parseInt((9 / 10) * outer_width, 10);
        }

        var duration = $this.data("duration");
        var li_number = $this.find("li").length;
        var $ul = $this.find("ul");
        var $li = $ul.find("li");
        $li.removeClass("active");

        if (outer_width < 760) {
          $li.css("width", container_width);
          $li.eq(1).addClass("active");
          if ($li.eq(3).hasClass("active")) {
            $li.eq(3).removeClass("active");
          }
        } else if (outer_width > 760) {
          $li.eq(1).addClass("active");
          $li.eq(3).addClass("active");
        }

        var list_width = $this.find("li").outerWidth(true);
        var left_offset;

        left_offset = parseInt(
          list_width - (outer_width - container_width - 42) / 2,
          10
        );

